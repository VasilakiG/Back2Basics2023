#include <bits/stdc++.h> // библиотека за скоро сѐ

int main()
{
    float firstNumber = 35e3; //    "e" измеѓу означува степен на 10
    //                              во овој случај 10^3, што значи 35 * 10^3, 35 000

    double secondNumber = 12E4; //  "Е" го има истото значење, означува степен на 10
    //                              во овој случај 10^4, што значи 12 * 10^4, 120 000

    std::cout << firstNumber << std::endl;
    std::cout << secondNumber << std::endl;

    return 0;
}